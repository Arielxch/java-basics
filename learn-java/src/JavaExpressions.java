public class JavaExpressions {
    public static void main(String args[]){
        // expressions = operands & operations
        // operands = values, variables, numbers, quatity
        // operators = + - * / %

        int friends = 10;
        friends ++;
        friends = friends * 2;
        friends = friends % 3;
        double herFriend = friends / 2.0;
        System.out.println(friends);
        System.out.println(herFriend);

    }
}
